CREATE TABLE IF NOT EXISTS `evaluation_outcome` (
  `id` bigint unsigned NOT NULL COMMENT 'canonical evaluation outcome id',
  `assessment_id` bigint unsigned NOT NULL COMMENT 'assessment id',
  `evaluation_run_id` varchar(128) NOT NULL COMMENT 'successful evaluation run id',
  `model_kind` varchar(50) NOT NULL COMMENT 'model kind',
  `model_sub_kind` varchar(50) DEFAULT NULL COMMENT 'model sub kind',
  `model_algorithm` varchar(50) DEFAULT NULL COMMENT 'model algorithm',
  `model_code` varchar(100) NOT NULL COMMENT 'model code',
  `model_version` varchar(50) NOT NULL COMMENT 'model version',
  `model_title` varchar(255) DEFAULT NULL COMMENT 'model title snapshot',
  `algorithm_family` varchar(50) DEFAULT NULL COMMENT 'evaluation algorithm family',
  `decision_kind` varchar(50) DEFAULT NULL COMMENT 'evaluation decision kind',
  `payload_format` varchar(100) DEFAULT NULL COMMENT 'evaluation payload format',
  `input_snapshot_ref` varchar(200) DEFAULT NULL COMMENT 'stable input snapshot reference',
  `payload_json` longtext NOT NULL COMMENT 'versioned canonical AssessmentOutcome JSON',
  `schema_version` int unsigned NOT NULL DEFAULT '1' COMMENT 'payload schema version',
  `evaluated_at` datetime(3) NOT NULL COMMENT 'evaluation completed at',
  `created_at` datetime(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) COMMENT 'created at',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_evaluation_outcome_assessment_id` (`assessment_id`),
  UNIQUE KEY `uk_evaluation_outcome_run_id` (`evaluation_run_id`),
  KEY `idx_evaluation_outcome_model` (`model_kind`, `model_code`, `model_version`),
  KEY `idx_evaluation_outcome_evaluated_at` (`evaluated_at`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='immutable canonical evaluation outcomes';
